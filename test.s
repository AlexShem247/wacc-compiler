Directive(text)
Label(main):
	Push(Register(rbp))
	Push(Register(rbx))
	MovRegister(Register(rsp),Register(rbp))
	MovImm(Immediate(7),Register(rax))
	MovRegister(Register(rax),Register(rdi))
	Call(Label(exit))
	MovImm(Immediate(0),Register(rax))
	Pop(Register(rbx))
	Pop(Register(rbp))
	Ret()

Directive(text)
Label(exit):
	Push(Register(rbp))
	MovRegister(Register(rsp),Register(rbp))
	Align(Register(rsp))
	Call(Label(exit@plt))
	MovRegister(Register(rbp),Register(rsp))
	Pop(Register(rbp))
	Ret()
