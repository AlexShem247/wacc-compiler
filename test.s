Directive(text)
Label(main):
	Push(StackPointer())
	Push(scratchReg1())
	MovRegister(StackPointer(),BasePointer())
	MovImm(Immediate(-8),ReturnRegister())
	MovRegister(ReturnRegister(),paramReg1())
	Call(Label(exit))
	MovImm(Immediate(0),ReturnRegister())
	Pop(scratchReg1())
	Pop(BasePointer())
	Ret()

